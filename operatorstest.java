class operatorstest{
	public static void main(String[] args){
		int num1 = 5;
		float num2 = 10f;
		// try operators
		System.out.println(num1*num2); // *
		System.out.println(num1/num2); // / 
		System.out.println(num1%num2); // %
		System.out.println(num1+num2); // +
		System.out.println(num1-num2); // -
		System.out.println(num1<num2); // <
		System.out.println(num1>num2); // >
		System.out.println(num1==num2); // ==
		System.out.println(num1!=num2); // !=
	}
}
